function recodeEducation(instance){

  var health_recode = ['very poor','poor','moderate','good','excellent'];
  var fam_recode = ['very bad', 'bad', 'moderate', 'good', 'excellent'];
  var study_recode = ['<2 hrs', '2-5 hrs', '5-10 hrs', '>10 hrs'];
  var alc_recode = ['very low', 'low', 'moderate', 'high', 'very high'];
  //education levels seem odd, especially to American participants, probably. Recoding as something more typical
  var parent_recode = ['none','some high school','high school','college', 'graduate school']
  //job categories odd too - changing "at_home" to "unemployed"

  instance.Health = health_recode[instance.Health-1];
  instance["Fam Relations"] = fam_recode[instance["Fam Relations"] - 1];
  instance["Study Time"] = study_recode[instance["Study Time"] - 1];
  instance.Alcohol = alc_recode[instance.Alcohol - 1];
  instance["M's Ed"] = parent_recode[instance["M's Ed"]];
  instance["F's Ed"] = parent_recode[instance["F's Ed"]];

  if (instance["F's Job"] == "at_home"){
    instance["F's Job"] = "unemployed"
  } else if (instance["F's Job"] == "services"){
    instance["F's Job"] = "civ service"
  }

  return instance
}


