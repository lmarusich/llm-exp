function recodeCredit(instance){

  //there aren't actually any "0"s in our dataset, so I'm leaving off the lowest categor
  var job_recode = ['unskilled','skilled','highly skilled'];
  instance.Job = job_recode[instance.Job - 1];

  if (instance.Purpose == "radio/TV"){
    instance.Purpose = "TV"
  } else if (instance.Purpose == "furniture/equipment"){
    instance.Purpose = "furniture"
  }

  if (instance.Housing == "free"){
    instance.Housing = "subsidized";
  }

  if (instance.Checking == "rich"){
    instance.Checking = "high"
  } else if (instance.Checking == "quite rich"){
    instance.Checking = "very high"
  }

  if (instance.Saving == "rich"){
    instance.Saving = "high"
  } else if (instance.Saving == "quite rich"){
    instance.Saving = "very high"
  }

  return instance
}

